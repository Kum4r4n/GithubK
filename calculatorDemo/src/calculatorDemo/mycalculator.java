package calculatorDemo;

public class mycalculator {
	int num1=20;
	int num2=50;
	
	public void addnum() {
		
		int result=num1+num2;
		System.out.println("sum of numbers " + result);
		
	}
	
	public void subnum() {
		
		int result=num1-num2;
		System.out.println("sub of numbers " + result);
		
	}
	
	public void divnum() {
		
		int result=num1/num2;
		System.out.println("div of numbers " + result);
		
	}
	
	public void mulnum() {
		
		int result=num1*num2;
		System.out.println("multi of numbers " + result);
		
	}

}
